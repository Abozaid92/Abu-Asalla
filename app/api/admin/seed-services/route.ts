export async function GET(request: NextRequest) {
  const secret = request.nextUrl.searchParams.get("secret");
  if (!secret || secret !== process.env.SEED_SECRET) {
    return NextResponse.json({ error: "غير مصرح" }, { status: 401 });
  }

  const created: string[] = [];
  const skippedExisting: string[] = [];

  for (const svc of SERVICES) {
    const existing = await prisma.service.findUnique({ where: { slug: svc.slug } });
    if (existing) {
      skippedExisting.push(svc.slug);
      continue;
    }

    await prisma.service.create({
      data: {
        name: svc.name,
        slug: svc.slug,
        // محتوى مؤقت placeholder بس — لازم يتستبدل بمحتوى فريد 1600-2400 كلمة
        // فعلي قبل الإطلاق (راجع PROMPT_ARTICLES.md لنفس منطق البحث عن
        // كلمات مفتاحية حقيقية، الأسلوب المطلوب هنا مشابه لصفحات الخدمة).
        content: `محتوى مؤقت لصفحة "${svc.name}" — يُستبدل بمحتوى فريد كامل قبل الإطلاق.`,
        heroImage: "",
        gallery: [],
        isActive: svc.confirmed, // الخدمات غير المؤكدة تتزرع لكن isActive: false لحد ما تتأكد من العميل
        metaTitle: `${svc.name} في الرياض | أبو أصالة للدهانات والديكور`,
        metaDescription: `${svc.name} في الرياض بأعلى جودة تنفيذ. اطلب عرض سعر مخصص أو تواصل واتساب فورًا.`,
        keywords: [svc.name, "الرياض"],
      },
    });

    created.push(svc.slug);
  }

  revalidateTag("services");

  return NextResponse.json({
    createdCount: created.length,
    created,
    skippedExistingCount: skippedExisting.length,
    note: "الخدمات غير المؤكدة (جبس بورد، عزل صوت وحراري) اتزرعت بحالة isActive:false — فعّلها من الأدمن بس بعد ما تتأكد من العميل إنها خدمة حقيقية بيقدمها.",
  });
}
